# Write a function `divisors(x)` that will return the list of all proper
# divisiors of a given number `x`. These are all divisiors that are smaller
# than `x`.
#
# Example:
#
#     divisiors(12)
#
# should return:
#
#     [1, 2, 3, 4, 6]
# =============================================================================

def divisors(x):
    cntlst = []
    for i in range(1,x):
        if x % i == 0:
            cntlst.append(i)
    return cntlst

