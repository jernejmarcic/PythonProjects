# 5. podnaloga
# We can also define [BMI](https://en.wikipedia.org/wiki/Body_mass_index) of
# a given number `i` as `bmi = sum_of_divisors(i)/i`. Deficient numbers have
# BMI less than 1, perfect numbers have BMI equal to 1 and abundant numbers
# have BMI greater than 1.
#
# Write a function `maxabundant(a, b)` that should return
# [tuple](https://docs.python.org/3/tutorial/datastructures.html#tuples-and-sequences)
# `(i, bmi)` for the number `i` between numbers `a` and `b` that has the
# highest BMI.
# =============================================================================

def maxabundant(a, b):
    maxi = a
    maxbmi = sum_of_divisors(maxi)/maxi
    for i in range(a,b+1):
        bmi = sum_of_divisors(i)/i
        if maxbmi < bmi:
            maxbmi = bmi
            maxi = i

    return (maxi, maxbmi)


        
        



































































































