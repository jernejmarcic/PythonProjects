# function, use functions from previous sub-problems.
# =============================================================================
def number_weight(x):
    if sum_of_divisors(x) > x:
        return "abundant"
    elif sum_of_divisors(x) < x:
        return "deficient"
    elif sum_of_divisors(x) == x:
        return "perfect"
