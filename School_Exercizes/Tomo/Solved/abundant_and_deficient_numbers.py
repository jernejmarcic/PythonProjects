# 3. podnaloga
# [Abundant number](https://en.wikipedia.org/wiki/Abundant_number) is a
# number for which the sum of its proper divisors is greater than the number
# itself. [Deficient number](https://en.wikipedia.org/wiki/Deficient_number)
# is a number for which the sum of its proper divisors is greater than the
# number itself.
# And finally [perfect number](https://en.wikipedia.org/wiki/Perfect_number)
# is a number for which the sum of its proper divisors is equal to the number
# itself.
#
# Write a function `number_weight(x)` which will return string `'abundant'`,
